
//== Equal to
#include<stdio.h>
int main()
{
   int x = 4;
   int y = 4;
    printf ("%d", x==y);
    return 0;
}


//!= Not Equal To
#include<stdio.h>
int main()
{
   int x = 4;
   int y = 5;
    printf ("%d", x!=y);
    return 0;
}

// > Greater than
#include<stdio.h>
int main()
{
   int x = 5;
   int y = 4;
    printf ("%d", x>y);
    return 0;
}


// < Lesser Than
#include<stdio.h>
int main()
{
   int x = 4;
   int y = 5;
    printf ("%d", x<y);
    return 0;
}


// >= Greater than or equal to
#include <stdio.h>
int main() {
  int x = 5;
  int y = 3;
// Returns 1 (true) because five is greater than, or equal, to 3
  printf("%d", x >= y);
  return 0;
}


// <= Less than or equal to
#include <stdio.h>
int main() {
  int x = 5;
  int y = 3;
// Returns 0 (false) because 5 is neither less than or equal to 3
  printf("%d", x <= y);
  return 0;
}
