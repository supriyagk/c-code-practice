// = (BASIC ASSIGNMENT)
#include <stdio.h>
int main()
{
    int x = 5;
    printf ("%d", x);
    return 0;
}


// += (ADDITION ASSIGNMENT)
#include <stdio.h>
int main()
{
    int x = 5;
     x += 3;
    printf ("%d", x);
    return 0;
}

// -= (SUBTRACTION ASSIGNMENT)
#include <stdio.h>
int main()
{
    int x = 5;
    x -= 3;
    printf ("%d",x);
    return 0;
}


//*= (MULTIPLICATION ASSIGNMENT)
#include <stdio.h>
